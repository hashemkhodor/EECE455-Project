def binToInt(binary):
    NUM=0
    for i in range(len(binary)-1,-1,-1):
        NUM += int(binary[i])<<(len(binary)-i-1)
    return NUM
def IntTobin(integer,size):
    BIN=[]
    
    while(integer>0):
        BIN.append(f"{integer%2}")
        integer//=2
    
    BIN.reverse()
    
    return ''.join( (['0' for i in range(size-len(BIN))] if size>len(BIN) else [])+ BIN)



